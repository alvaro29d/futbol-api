package com.segurola.futbol.domain;

public class Noticia {
	private int id_noticia;
	private String titulo_noticia;
	private String subtitulo_noticia;
	private String cuerpo_noticia;
	private String foto_noticia;
	private String comentario_noticia;
	private int id_torneo_noticia;
	
	public int getId_noticia() {
		return id_noticia;
	}
	public void setId_noticia(int id_noticia) {
		this.id_noticia = id_noticia;
	}
	public String getTitulo_noticia() {
		return titulo_noticia;
	}
	public void setTitulo_noticia(String titulo_noticia) {
		this.titulo_noticia = titulo_noticia;
	}
	public String getSubtitulo_noticia() {
		return subtitulo_noticia;
	}
	public void setSubtitulo_noticia(String subtitulo_noticia) {
		this.subtitulo_noticia = subtitulo_noticia;
	}
	public String getCuerpo_noticia() {
		return cuerpo_noticia;
	}
	public void setCuerpo_noticia(String cuerpo_noticia) {
		this.cuerpo_noticia = cuerpo_noticia;
	}
	public String getFoto_noticia() {
		return foto_noticia;
	}
	public void setFoto_noticia(String foto_noticia) {
		this.foto_noticia = foto_noticia;
	}
	public String getComentario_noticia() {
		return comentario_noticia;
	}
	public void setComentario_noticia(String comentario_noticia) {
		this.comentario_noticia = comentario_noticia;
	}
	public int getId_torneo_noticia() {
		return id_torneo_noticia;
	}
	public void setId_torneo_noticia(int id_torneo_noticia) {
		this.id_torneo_noticia = id_torneo_noticia;
	}
	
	
}
